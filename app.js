// Configura√ß√£o CSRF ultra simplificada para garantir funcionamento em todos os ambientes
// Implementa√ß√£o mais simples e robusta
const simpleCsrf = (req, res, next) => {
  // Se for requisi√ß√£o HEAD ou OPTIONS, pular verifica√ß√£o CSRF
  if (req.method === 'HEAD' || req.method === 'OPTIONS' || 
      req.path === '/health' || req.path.startsWith('/api/')) {
    return next();
  }

  // Em ambiente de desenvolvimento, ser mais estrito
  // Em produ√ß√£o, ser mais tolerante para evitar problemas com Render
  const isDev = process.env.NODE_ENV !== 'production';

  // Criar um token para o usu√°rio se ainda n√£o existir
  if (!req.session._csrfToken) {
    // Token simples, mas suficiente para prote√ß√£o b√°sica
    req.session._csrfToken = Math.random().toString(36).substring(2, 15) + 
                            Math.random().toString(36).substring(2, 15);
    // Salvar a sess√£o explicitamente para garantir que o token seja persistido
    if (req.session.save) {
      req.session.save();
    }
  }
  
  // M√©todo para gerar tokens
  req.csrfToken = function() {
    return req.session._csrfToken;
  };
  
  // Em m√©todos que modificam dados (POST, PUT, DELETE), verificar o token
  if (req.method === 'GET') {
    // Para GET, apenas disponibilizar o token
    return next();
  } else {
    // Para outros m√©todos, verificar o token
    // Verificar token do corpo do form, ou do header X-CSRF-Token
    const token = req.body._csrf || req.headers['x-csrf-token'] || req.headers['csrf-token'];
    
    // Em produ√ß√£o, permitir login sem token CSRF para evitar falhas de UX
    if (req.path === '/auth/login' && !isDev) {
      console.log('‚ö†Ô∏è Login em produ√ß√£o: CSRF bypass ativado para melhorar experi√™ncia do usu√°rio');
      return next();
    }
    
    if (token !== req.session._csrfToken && isDev) {
      // Token inv√°lido (apenas tratar como erro em desenvolvimento)
      console.error('‚ö†Ô∏è Erro CSRF: token inv√°lido');
      console.error(`  Caminho: ${req.path}`);
      console.error(`  M√©todo: ${req.method}`);
      
      // Flash message e redirecionamento para login
      req.flash('error_msg', 'Sess√£o expirada ou inv√°lida. Por favor, fa√ßa login novamente.');
      
      // Responder de acordo com o tipo de requisi√ß√£o
      if (req.xhr || req.headers.accept?.includes('application/json')) {
        return res.status(403).json({ error: 'Sess√£o expirada ou inv√°lida.' });
      }
      
      return res.redirect('/auth/login');
    }
    
    next();
  }
};

